    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String rollNo = jTextField2.getText();
        String gender = (String)jComboBox1.getSelectedItem();
        String fatherName = jTextField3.getText();
        String course = (String)jComboBox3.getSelectedItem();
        String branchName = (String)jComboBox2.getSelectedItem();
        
        try{
            Connection conn = ConnectionProviderCode.getcon();
            Statement st = conn.createStatement();
            String query = "insert into student values('"+name+"', '"+rollNo+"', '"+gender+"', '"+fatherName+"', '"+course+"', '"+branchName+"')";
            st.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "Insertion Successful!");
            setVisible(false);
            InsertStudent is = new InsertStudent();
            is.setVisible(true);
            is.setLocationRelativeTo(null);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }                                        

